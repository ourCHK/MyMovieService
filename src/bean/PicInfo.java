package bean;

public class PicInfo {
	String picName;
	String picPath;
	public String getPicName() {
		return picName;
	}
	public void setPicName(String picName) {
		this.picName = picName;
	}
	public String getPicPath() {
		return picPath;
	}
	public void setPicPath(String picAddress) {
		this.picPath = picAddress;
	}
	
}
